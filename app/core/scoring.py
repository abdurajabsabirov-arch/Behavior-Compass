def generate_context_result(scores: dict, context: str) -> str:
    """Return context-specific Telegram HTML result for the exact profile key."""
    profile_key = get_profile_key(scores)["profile_key"]
    if context not in CONTEXT_BLOCKS:
        raise ValueError(f"Unknown context: {context}")
    try:
        return CONTEXT_BLOCKS[context][profile_key]
    except KeyError as exc:
        raise ValueError(f"Missing context block for {context}:{profile_key}") from exc


def generate_result(scores: dict, context: str | None = None) -> str:
    if context is None:
        return generate_universal_result(scores)
    return generate_context_result(scores, context)


def validate_profile_blocks() -> None:
    checks = {
        "SINGLE_PROFILE_DESCRIPTIONS": (SINGLE_PROFILE_DESCRIPTIONS, SINGLE_PROFILE_KEYS),
        "MIXED_PROFILE_DESCRIPTIONS": (MIXED_PROFILE_DESCRIPTIONS, MIXED_PROFILE_KEYS),
        "PROFILE_NAMES": (PROFILE_NAMES, ALL_PROFILE_KEYS),
        "MANAGEMENT_BLOCKS": (MANAGEMENT_BLOCKS, ALL_PROFILE_KEYS),
        "SALES_BLOCKS": (SALES_BLOCKS, ALL_PROFILE_KEYS),
        "TEAM_BLOCKS": (TEAM_BLOCKS, ALL_PROFILE_KEYS),
        "COMMUNICATION_BLOCKS": (COMMUNICATION_BLOCKS, ALL_PROFILE_KEYS),
        "STRESS_BLOCKS": (STRESS_BLOCKS, ALL_PROFILE_KEYS),
        "ADVICE_BLOCKS": (ADVICE_BLOCKS, ALL_PROFILE_KEYS),
        "LOWEST_STYLE_BLOCKS": (LOWEST_STYLE_BLOCKS, SINGLE_PROFILE_KEYS),
    }
    errors = []
    for name, (mapping, required_keys) in checks.items():
        missing = sorted(required_keys - set(mapping))
        extra = sorted(set(mapping) - required_keys)
        if missing:
            errors.append(f"{name} missing: {missing}")
        if extra and name in {"SINGLE_PROFILE_DESCRIPTIONS", "MIXED_PROFILE_DESCRIPTIONS"}:
            errors.append(f"{name} extra: {extra}")
    if errors:
        raise ValueError("; ".join(errors))


class ScoringService:
    """Service for scoring answers and producing profile text."""

    @staticmethod
    def calculate_scores(answers: dict) -> dict:
        scores = {"blue": 0, "green": 0, "yellow": 0, "red": 0}

        for q_idx in BLUE_QUESTIONS:
            if q_idx in answers:
                scores["blue"] += answers[q_idx]
        for q_idx in GREEN_QUESTIONS:
            if q_idx in answers:
                scores["green"] += answers[q_idx]
        for q_idx in YELLOW_QUESTIONS:
            if q_idx in answers:
                scores["yellow"] += answers[q_idx]
        for q_idx in RED_QUESTIONS:
            if q_idx in answers:
                scores["red"] += answers[q_idx]

        max_score = max(
            len(BLUE_QUESTIONS) * 5,
            len(GREEN_QUESTIONS) * 5,
            len(YELLOW_QUESTIONS) * 5,
            len(RED_QUESTIONS) * 5,
        )

        for color in scores:
            scores[color] = round((scores[color] / max_score) * 100)

        return scores

    sort_scores = staticmethod(sort_scores)
    get_level = staticmethod(get_level)
    get_profile_key = staticmethod(get_profile_key)
    generate_score_table = staticmethod(generate_score_table)
    generate_universal_result = staticmethod(generate_universal_result)
    generate_context_result = staticmethod(generate_context_result)
    generate_result = staticmethod(generate_result)
    validate_profile_blocks = staticmethod(validate_profile_blocks)

    @staticmethod
    def determine_primary_secondary(scores: dict) -> tuple:
        sorted_colors = sort_scores(scores)
        return sorted_colors[0][0], sorted_colors[1][0]

    @staticmethod
    def get_profile_description(language: str, color: str) -> dict:
        return {
            "name": PROFILE_NAMES[color],
            "description": SINGLE_PROFILE_DESCRIPTIONS[color],
        }


