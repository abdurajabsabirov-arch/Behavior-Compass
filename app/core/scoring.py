def style_label(style: str) -> str:
    meta = STYLE_META[style]
    return f"{meta['emoji']} {meta['label']} — {meta['title']}"


def get_level(score: int) -> str:
    if score <= 30:
        return "слабо выражен"
    if score <= 50:
        return "проявляется ситуативно"
    if score <= 70:
        return "заметно выражен"
    return "ярко выражен"


def sort_scores(scores: dict) -> list:
    """Return (style, score) tuples sorted from highest to lowest score."""
    return sorted(scores.items(), key=lambda item: (-item[1], STYLE_ORDER.index(item[0])))


def get_profile_key(scores: dict) -> dict:
    """Return profile metadata for the score set."""
    sorted_scores = sort_scores(scores)
    primary_style, primary_score = sorted_scores[0]
    secondary_style, secondary_score = sorted_scores[1]
    lowest_style, lowest_score = sorted_scores[-1]
    difference = primary_score - secondary_score

    if difference <= MIXED_THRESHOLD:
        profile_type = "mixed"
        profile_key = f"{primary_style}_{secondary_style}"
    else:
        profile_type = "single"
        profile_key = primary_style

    return {
        "profile_type": profile_type,
        "profile_key": profile_key,
        "primary_style": primary_style,
        "secondary_style": secondary_style,
        "lowest_style": lowest_style,
        "lowest_score": lowest_score,
        "difference": difference,
    }


def generate_score_table(scores: dict) -> str:
    """Return a Telegram HTML score table ordered by score."""
    return "\n".join(
        f"{style_label(style)}: <b>{score}/100</b> — {get_level(score)}"
        for style, score in sort_scores(scores)
    )


