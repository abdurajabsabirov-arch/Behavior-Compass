def get_text(language: str, key: str, **kwargs) -> str:
    """Получить перевод текста"""
    if language not in TRANSLATIONS:
        language = "ru"
    
    text = TRANSLATIONS[language].get(key, f"[{key}]")
    
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    
    return text
